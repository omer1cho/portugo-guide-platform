-- מיגרציה: הוספת flag is_guide לטבלת guides
--
-- מה השתנה:
--   • עמודה חדשה is_guide BOOLEAN — האם המשתמש הזה הוא מדריך פעיל
--     שמשבצים אותו על סיורים? (להבדיל מאדמין-בלבד כמו עומר)
--   • ברירת מחדל true — כל המדריכים הקיימים נשארים מדריכים
--   • עומר/פורטוגו — מתעדכנים ל-false (אדמין-בלבד, לא משבצים אותם)
--   • רונה — מתעדכנת ל-is_guide=true + city='lisbon' + qualified_tours לכל הסיורים
--
-- הקשר:
--   • עד היום הסינון בקוד היה "כל מי שהוא is_admin=false" — חסם את רונה
--   • עכשיו הסינון יהיה "כל מי שהוא is_guide=true" — כולל אדמינים שגם מדריכים
--
-- אופן הרצה:
--   Supabase Dashboard → SQL Editor → להדביק → Run

-- ───────────────────────────────────────────────────────────
-- 1. עמודה חדשה
-- ───────────────────────────────────────────────────────────
alter table guides
  add column if not exists is_guide boolean not null default true;

-- ───────────────────────────────────────────────────────────
-- 2. עומר וכל אדמין שאינו מדריך/ה — לסמן is_guide=false
-- ───────────────────────────────────────────────────────────
-- אם יש אדמינים נוספים שאינם מדריכים, להוסיף את שמותיהם לרשימה כאן.
update guides
   set is_guide = false
 where name in ('עומר', 'פורטוגו');

-- ───────────────────────────────────────────────────────────
-- 3. רונה — אדמין שגם מדריכה. להגדיר עיר + הסמכות + להפעיל
-- ───────────────────────────────────────────────────────────
-- הערה: הפקודה הזו תרוץ רק אם רונה כבר קיימת בטבלה כ-admin.
-- אם name של רונה ב-DB שונה מ-'רונה' (למשל "רונה כהן"), עדכני בהתאם.
update guides
   set is_guide = true,
       is_active = true,
       city = 'lisbon',
       qualified_tours = array['קלאסי_1','בלם_1','סינטרה','אראבידה','אובידוש','קולינרי','פרטי_1']
 where name ilike 'רונה%';

-- ───────────────────────────────────────────────────────────
-- 4. אישור — מציג מי is_guide=true (אלה יופיעו ב-dropdown של שיבוצים)
-- ───────────────────────────────────────────────────────────
select name, city, is_admin, is_guide, is_active, qualified_tours
  from guides
 where is_guide = true
 order by name;
